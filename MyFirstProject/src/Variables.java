public class Variables {

    public static void main(String [] args){

        // Camel case is used for variables.
        //int age;  This is declaration
        //age = 22;  This is initialization

        int age = 22;
        System.out.println("Age is: " + age);

        double price = 23.12;
        System.out.println("Price is: " + price);

        char grade = 'A';
        System.out.println("Grade is: " + grade);

        boolean isStudent = true;
        boolean forSale = false;
        System.out.println(isStudent);

        String name = "Neha";   //Series of characters
        String language = "Java";
        System.out.println("My name is: " + name);  // This is string concatenation

        System.out.println("I'm " + name + " my age is " + age + " and I love " + language);
    }
}
